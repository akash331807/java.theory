public class Primivtive
{
    public static void main(String[] args) {
        // Declare and initialize primitive data types
        char myChar = 'A';        // character type
        int myInt = 100;          // integer type
        byte myByte = 120;        // byte type
        short myShort = 1500;     // short type
        float myFloat = 5.75f;    // float type
        double myDouble = 19.99;  // double type
        long myLong = 123456789L; // long type

        // Print the values of each variable
        System.out.println("Character: " + myChar);
        System.out.println("Integer: " + myInt);
        System.out.println("Byte: " + myByte);
        System.out.println("Short: " + myShort);
        System.out.println("Float: " + myFloat);
        System.out.println("Double:" + myDouble);
        System.out.println("Long: " +myLong);
    }
}
